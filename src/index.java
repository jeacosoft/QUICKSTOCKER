    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        int conf = JOptionPane.showConfirmDialog(this, "Are you sure you want to delete this Item", "Warning",JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
        String combo = (String)citem.getSelectedItem();
        try {
            //delete item
            String sql = "delete  from `admintable` where ItemName = '"+combo+"'";
            ps = con.prepareStatement(sql);
            int del = ps.executeUpdate();
            if(conf == JOptionPane.YES_OPTION){
                if(del> 0){

                    JOptionPane.showMessageDialog(this,"Item deleted successfully");
                    fetchdata();
                    citemdata();
                }
            }
            else{}
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this,"Item could not be deleted, ensure you selected the item to be deleted");
            Logger.getLogger( index.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton7ActionPerformed

