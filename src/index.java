public void analysis(){

 try {
            String date = ((JTextField)mydate.getDateEditor().getUiComponent()).getText();
            String s = "SELECT * FROM `dailyanalysistable` WHERE Date = '"+date+"'";
            ps = con.prepareStatement(s);
            rs = ps.executeQuery();
            if(rs.next()){
               
                dailysale.setText("N" + rs.getString("TotalDailySales"));
               
                texp.setText("N" + rs.getString("TotalExpenses"));
               
                tbal.setText("N" + rs.getString("NetBalance"));
                
            }       } catch (SQLException ex) {
            Logger.getLogger(vendor.class.getName()).log(Level.SEVERE, null, ex);
        }



}
